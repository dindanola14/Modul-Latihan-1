public class PenerapanFor {
   public static void main(String[] args){
       for(int a=1; a<=1; a++){
           for(int i=1; i<=4; i++){
               System.out.print("@");
           }
           System.out.println();
       }
       for(int j=1; j<=2; j++){
           for(int k=1; k<2; k++){
               System.out.print("@");
           }
           for(int l=1; l<=2; l++){
               System.out.print(" ");
           }
           for(int m=1; m<2; m++){
               System.out.print("@");
           }
           System.out.println();
       }
       for(int b=1; b<=1; b++){
           for(int i=1; i<=4; i++){
               System.out.print("@");
           }
           System.out.println();
       }
   } 
}
